# Basic Numerical data types => int, float, complex

a = 10                        
print("\n",a , " = ", type(a))     # Integer variable 
a = 10.5
print()      
print(a, " = ", type(a))      # Float Variable for decimal values
a = 4 + 5j                  # a + bj is used to declare complex types 
print()      
print(a, " = ", type(a))              #a,b are variables and j represents imaginary part.







